'''
Modules are just Python (.py) files

As programs grow larger, it makes sense to break them up into separate files (modules).
Modules can be imported into other modules using the keyword 'import'
'''

# import the entire contents of a module into a single variable
# keyword: import
# import <MODULE_NAME>


# module attributes are available using dot notation

# --------------------------------------------------------------------------------------- #

# import specific attributes from a module
# keyword: from, import
# from <MODULE_NAME> import <ATTRIBUTE_1>, <ATTRIBUTE_2>, ...

# dot notation is no longer needed because the variables exist in this file

# ---------------------------------------------------------------------- #

# import specific attributes from a module and change their name
# keyword: from, import, as
# from <MODULE_NAME> import <ATTRIBUTE_1> as <VARIABLE_1>, ...


